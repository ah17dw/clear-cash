import { useState, useEffect, createContext, useContext, ReactNode } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { toast } from 'sonner';

// Authorized email addresses - only these can access the app
const AUTHORIZED_EMAILS = ['alex@hayesalex.com'];

interface AuthContextType {
  user: User | null;
  session: Session | null;
  loading: boolean;
  signOut: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function AuthProvider({ children }: { children: ReactNode }) {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);

  const checkAuthorization = async (currentUser: User | null): Promise<User | null> => {
    if (!currentUser) return null;
    
    const email = currentUser.email?.toLowerCase();
    if (!email || !AUTHORIZED_EMAILS.includes(email)) {
      // Sign out unauthorized users
      await supabase.auth.signOut();
      toast.error('Access denied. Your email is not authorized.');
      return null;
    }
    
    return currentUser;
  };

  useEffect(() => {
    // Set up auth state listener FIRST
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        setSession(session);
        const authorizedUser = await checkAuthorization(session?.user ?? null);
        setUser(authorizedUser);
        setLoading(false);
      }
    );

    // THEN check for existing session
    supabase.auth.getSession().then(async ({ data: { session } }) => {
      setSession(session);
      const authorizedUser = await checkAuthorization(session?.user ?? null);
      setUser(authorizedUser);
      setLoading(false);
    });

    return () => subscription.unsubscribe();
  }, []);

  const signOut = async () => {
    await supabase.auth.signOut();
  };

  return (
    <AuthContext.Provider value={{ user, session, loading, signOut }}>
      {children}
    </AuthContext.Provider>
  );
}

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
}
